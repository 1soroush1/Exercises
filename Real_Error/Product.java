public class Product {
    int id;
    int size;
    int pressureTolerance;
    int colorTransparency;
    Product(int id, int size, int pressureTolerance, int colorTransparency) {
        this.id = id;
        this.size = size;
        this.pressureTolerance = pressureTolerance;
        this.colorTransparency = colorTransparency;
    }
}
